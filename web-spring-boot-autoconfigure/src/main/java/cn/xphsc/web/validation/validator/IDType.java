package cn.xphsc.web.validation.validator;


public enum IDType
{
	IDCARD("身份证"),
	TAINWAESENCARD("台胞证"),
	RETURNPERMITCARD("回乡证"),
	PASSPORT("护照"), // 护照
	HONEKONGPASS("港澳通行证"),
	RESIDENCEPERMIT("居住证");
	private String value;

	private IDType(String value)
	{
		this.value = value;
	}

	public String getValue()
	{
		return value;
	}

	public void setValue(String value)
	{
		this.value = value;
	}

}
